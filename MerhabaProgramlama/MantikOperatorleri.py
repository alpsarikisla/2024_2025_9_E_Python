# Mantık Operatörleri
# and
# True  and True    = True
# True  and False   = False
# False and True    = False
# False and False   = False
sayi = 11
# sayı değişkeni içindeki veri 10 ile 20 arasında mı?
araliktaMi = sayi > 10 and sayi < 20
print("Sayı 10 ile 20 arasında mı?")
print(araliktaMi)

# Sayı değişkeni içindeki veri 10 ile 20 aralığı dışında mı?
aralikDisiMi = sayi < 10 or sayi > 20
print("Sayı 10 ile 20 aralığı dışında mı?")
print(aralikDisiMi)



