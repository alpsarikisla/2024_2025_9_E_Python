sayilar = [3,4,5,5,5,8,19,45,5,5,7]
print(len(sayilar))#eleman sayısı

#dizi sonuna eleman ekleme
sayilar.append(3)
print(sayilar)

#Diziden sondaki elemanını silme
sayilar.pop()
print(sayilar)

#dizideki istediğim index numarasındaki elemanı silme
sayilar.pop(2)
print(sayilar)

#sıralama yapma
sayilar.sort()
print(sayilar)

#Dizi eleman sıralamsını ters çevirme
sayilar.reverse()
print(sayilar)