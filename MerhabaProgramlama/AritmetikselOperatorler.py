# Operatörler
# Aritmetiksel Operatörler
# =  Atama operatörü
# +  Toplama işlem operatörü *Sayısal Veri
# +  Yanına Ekleme operatörü *Metinsel Veri
# -  Çıkarma operatörü
# *  Çarpma operatörü
# /  Bölme operatörü
# %  Mod alma(Bölümden Kalan) operatörü
sayi1 = 11
sayi2 = 3
toplam = sayi1 + sayi2
print("Toplam = ")
print(toplam)
fark = sayi1 - sayi2
print("Fark = ")
print(fark)
carpim = sayi1 * sayi2
print("Çarpım = ")
print(carpim)
bolum = sayi1 / sayi2
print("Bölüm = ")
print(bolum)
mod = sayi1 % sayi2
print("Bölümünden Kalan = ")
print(mod)
