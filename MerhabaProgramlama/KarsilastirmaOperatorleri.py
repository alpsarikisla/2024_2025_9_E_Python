# Karşılaştırma Operatörleri
# Boolean(Bool) türünde veri döndürürler
# true - false

sayi1 = 11
sayi2 = 3

print("sayi1 değişkeni içindeki veri sayi2 değişkeni içindeki veriden")

buyukMu = sayi1 > sayi2
print("büyük mü?")
print(buyukMu)

buyukEsitMi = sayi1 >= sayi2
print("Büyük eşit mi?")
print(buyukEsitMi)

kucukMu = sayi1 < sayi2
print("Küçük mü?")
print(kucukMu)

kucukEsitMi = sayi1 <= sayi2
print("Küçük eşit mi?")
print(kucukEsitMi)

esitMi = sayi1 == sayi2
print("Eşit mi?")
print(esitMi)

esitDegilMi = sayi1 != sayi2
print("Eşit değil mi?")
print(esitDegilMi)
